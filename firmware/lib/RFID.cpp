/******************************************************************************
 * 魹ｽ魹ｽ魹ｽ魹ｽ RFID
 * int chipSelectPin RFID /ENABLE pin
 ******************************************************************************/
RFID::RFID(int chipSelectPin, int NRSTPD)
{
  _chipSelectPin = chipSelectPin;
  _NRSTPD = NRSTPD;

  pinMode(_chipSelectPin,OUTPUT);     // 魹ｽ魹ｽ魹ｽﾃｹﾜｽ魹ｽ_chipSelectPinﾎｪ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾓｵ魹ｽﾄ｣魹ｽ魹ｽﾊｹ魹ｽﾜｿ魹ｽ
  digitalWrite(_chipSelectPin, LOW);


  pinMode(_NRSTPD,OUTPUT);            // 魹ｽ魹ｽ魹ｽﾃｹﾜｽ魹ｽNRSTPDﾎｪ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾃｻ魹ｽ魹ｽ魹ｽ魹ｽ
  digitalWrite(_NRSTPD, HIGH);
}

