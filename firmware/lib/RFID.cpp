void RFID::antennaOff(void)
{
  unsigned char temp;

  temp = readMFRC522(TxControlReg);
  if (!(temp & 0x03))
  {
    clearBitMask(TxControlReg, 0x03);
  }
}

/******************************************************************************
 * 魹ｽ魹ｽ 魹ｽ魹ｽ 魹ｽ魹ｽ魹ｽ魹ｽcalculateCRC
 * 魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽMF522魹ｽ魹ｽ魹ｽ魹ｽCRC
 * 魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽpIndata--ﾒｪ魹ｽ魹ｽ魹ｽ魹ｽCRC魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾝ｣魹ｽlen--魹ｽ魹ｽ魹ｽﾝｳ魹ｽ魹ｽﾈ｣魹ｽpOutData--魹ｽ魹ｽ魹ｽ魹ｽ魹ｽCRC魹ｽ魹ｽ魹ｽ
 * 魹ｽ魹ｽ 魹ｽ魹ｽ ﾖｵ魹ｽ魹ｽ魹ｽ魹ｽ
 ******************************************************************************/
void RFID::calculateCRC(unsigned char *pIndata, unsigned char len, unsigned char *pOutData)
{
  unsigned char i, n;

  clearBitMask(DivIrqReg, 0x04);      //CRCIrq = 0
  setBitMask(FIFOLevelReg, 0x80);     //魹ｽ魹ｽFIFOﾖｸ魹ｽ魹ｽ
  //Write_MFRC522(CommandReg, PCD_IDLE);

  //魹ｽ魹ｽFIFO魹ｽ魹ｽﾐｴ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ
  for (i=0; i<len; i++)
    writeMFRC522(FIFODataReg, *(pIndata+i));
  writeMFRC522(CommandReg, PCD_CALCCRC);

  //魹ｽﾈｴ魹ｽCRC魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ
  i = 0xFF;
  do
  {
    n = readMFRC522(DivIrqReg);
    i--;
  }
  while ((i!=0) && !(n&0x04));      //CRCIrq = 1

  //魹ｽ魹ｽﾈ｡CRC魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ
  pOutData[0] = readMFRC522(CRCResultRegL);
  pOutData[1] = readMFRC522(CRCResultRegM);
}

/******************************************************************************
 * 魹ｽ魹ｽ 魹ｽ魹ｽ 魹ｽ魹ｽ魹ｽ魹ｽMFRC522ToCard
 * 魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽRC522魹ｽ魹ｽISO14443魹ｽ魹ｽﾍｨﾑｶ
 * 魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽcommand--MF522魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾖ｣魹ｽ
 *           sendData--ﾍｨ魹ｽ魹ｽRC522魹ｽ魹ｽ魹ｽﾍｵ魹ｽ魹ｽ魹ｽﾆｬ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ,
 *                     sendLen--魹ｽ魹ｽ魹ｽﾍｵ魹ｽ魹ｽ魹ｽ魹ｽﾝｳ魹ｽ魹ｽ魹ｽ
 *                     backData--魹ｽ魹ｽ魹ｽﾕｵ魹ｽ魹ｽﾄｿ魹ｽﾆｬ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾝ｣魹ｽ
 *                     backLen--魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾝｵ魹ｽﾎｻ魹ｽ魹ｽ魹ｽ魹ｽ
 * 魹ｽ魹ｽ 魹ｽ魹ｽ ﾖｵ魹ｽ魹ｽ魹ｽﾉｹ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽMI_OK
 ******************************************************************************/
unsigned char RFID::MFRC522ToCard(unsigned char command, unsigned char *sendData, unsigned char sendLen, unsigned char *backData, unsigned int *backLen)
{
  unsigned char status = MI_ERR;
  unsigned char irqEn = 0x00;
  unsigned char waitIRq = 0x00;
  unsigned char lastBits;
  unsigned char n;
  unsigned int i;

  switch (command)
  {
    case PCD_AUTHENT:   //魹ｽ魹ｽﾖ､魹ｽ魹ｽ魹ｽ魹ｽ
    {
      irqEn = 0x12;
      waitIRq = 0x10;
      break;
    }
    case PCD_TRANSCEIVE:  //魹ｽ魹ｽ魹ｽ魹ｽFIFO魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ
    {
      irqEn = 0x77;
      waitIRq = 0x30;
      break;
    }
    default:
      break;
  }

  writeMFRC522(CommIEnReg, irqEn|0x80); //魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾐｶ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ
  clearBitMask(CommIrqReg, 0x80);       //魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾐｶ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾎｻ
  setBitMask(FIFOLevelReg, 0x80);       //FlushBuffer=1, FIFO魹ｽ魹ｽﾊｼ魹ｽ魹ｽ

  writeMFRC522(CommandReg, PCD_IDLE);   //魹ｽﾞｶ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾈ｡魹ｽ魹ｽ魹ｽ魹ｽﾇｰ魹ｽ魹ｽ魹ｽ魹ｽ

  //魹ｽ魹ｽFIFO魹ｽ魹ｽﾐｴ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ
  for (i=0; i<sendLen; i++)
    writeMFRC522(FIFODataReg, sendData[i]);

  //ﾖｴ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ
  writeMFRC522(CommandReg, command);
  if (command == PCD_TRANSCEIVE)
    setBitMask(BitFramingReg, 0x80);    //StartSend=1,transmission of data starts

  //魹ｽﾈｴ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ
  i = 2000; //i魹ｽ魹ｽ魹ｽ魹ｽﾊｱ魹ｽ魹ｽﾆｵ魹ｽﾊｵ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽM1魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾈｴ魹ｽﾊｱ魹ｽ魹ｽ25ms
  do
  {
    //CommIrqReg[7..0]
    //Set1 TxIRq RxIRq IdleIRq HiAlerIRq LoAlertIRq ErrIRq TimerIRq
    n = readMFRC522(CommIrqReg);
    i--;
  }
  while ((i!=0) && !(n&0x01) && !(n&waitIRq));

  clearBitMask(BitFramingReg, 0x80);      //StartSend=0

  if (i != 0)
  {
    if(!(readMFRC522(ErrorReg) & 0x1B)) //BufferOvfl Collerr CRCErr ProtecolErr
    {
      status = MI_OK;
      if (n & irqEn & 0x01)
        status = MI_NOTAGERR;     //??

      if (command == PCD_TRANSCEIVE)
      {
        n = readMFRC522(FIFOLevelReg);
        lastBits = readMFRC522(ControlReg) & 0x07;
        if (lastBits)
          *backLen = (n-1)*8 + lastBits;
        else
          *backLen = n*8;

        if (n == 0)
          n = 1;
        if (n > MAX_LEN)
          n = MAX_LEN;

        //魹ｽ魹ｽﾈ｡FIFO魹ｽﾐｽ魹ｽ魹ｽﾕｵ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ
        for (i=0; i<n; i++)
          backData[i] = readMFRC522(FIFODataReg);
      }
    }
    else
      status = MI_ERR;
  }

  //SetBitMask(ControlReg,0x80);           //timer stops
  //Write_MFRC522(CommandReg, PCD_IDLE);

  return status;
}


/******************************************************************************
 * 魹ｽ魹ｽ 魹ｽ魹ｽ 魹ｽ魹ｽ魹ｽ魹ｽfindCard
 * 魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾑｰ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾈ｡魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾍｺ魹ｽ
 * 魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽreqMode--ﾑｰ魹ｽ魹ｽ魹ｽ魹ｽﾊｽ魹ｽ魹ｽ
 *           TagType--魹ｽ魹ｽ魹ｽﾘｿ魹ｽﾆｬ魹ｽ魹ｽ魹ｽ魹ｽ
 *                    0x4400 = Mifare_UltraLight
 *                    0x0400 = Mifare_One(S50)
 *                    0x0200 = Mifare_One(S70)
 *                    0x0800 = Mifare_Pro(X)
 *                    0x4403 = Mifare_DESFire
 * 魹ｽ魹ｽ 魹ｽ魹ｽ ﾖｵ魹ｽ魹ｽ魹ｽﾉｹ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽMI_OK
 ******************************************************************************/
unsigned char RFID::findCard(unsigned char reqMode, unsigned char *TagType)
{
  unsigned char status;
  unsigned int backBits;      //魹ｽ魹ｽ魹ｽﾕｵ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾎｻ魹ｽ魹ｽ

  writeMFRC522(BitFramingReg, 0x07);    //TxLastBists = BitFramingReg[2..0] ???

  TagType[0] = reqMode;
  status = MFRC522ToCard(PCD_TRANSCEIVE, TagType, 1, TagType, &backBits);

  if ((status != MI_OK) || (backBits != 0x10))
    status = MI_ERR;

  return status;
}

